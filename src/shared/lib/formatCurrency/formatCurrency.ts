export function formatCurrency(amount: number, currencyCode: string): string {
  const isoCode = CURRENCY_CODE_MAP[currencyCode] || 'USD'
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: isoCode,
    minimumFractionDigits: 0,
    maximumFractionDigits: 2,
  }).format(amount)
}
